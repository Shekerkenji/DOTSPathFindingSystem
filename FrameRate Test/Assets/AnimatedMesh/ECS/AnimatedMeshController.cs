using Unity.Entities;
using UnityEngine;

// ???????????????????????????????????????????????????????????????????????????????
//  AnimatedMeshController
//
//  Static API + optional MonoBehaviour wrapper.
//  Use the static methods from any System, MonoBehaviour, or Editor script.
//
//  ?? By index ??????????????????????????????????????????????????????????????
//  AnimatedMeshController.Play(entity, 2);
//  AnimatedMeshController.Play(entity, 2, loop: false);
//
//  ?? By name ???????????????????????????????????????????????????????????????
//  AnimatedMeshController.Play(entity, "Run_S");
//  AnimatedMeshController.Play(entity, "Idle", loop: true, forceRestart: true);
//
//  ?? Playback control ??????????????????????????????????????????????????????
//  AnimatedMeshController.Pause(entity);
//  AnimatedMeshController.Resume(entity);
//  AnimatedMeshController.Stop(entity);      // pause + rewind to frame 0
//
//  ?? State query (read-only, no allocation) ????????????????????????????????
//  AnimatedMeshController.GetState(entity, out AnimatedMeshState s);
//  AnimatedMeshController.GetClipName(entity, out string name);
//  AnimatedMeshController.GetClipCount(entity, out int count);
// ???????????????????????????????????????????????????????????????????????????????

public static class AnimatedMeshControllerECS
{
    // ?? Play by clip index ????????????????????????????????????????????????????

    /// <summary>Switch to the clip at <paramref name="clipIndex"/>.</summary>
    /// <param name="loop">Override the loop flag. Pass null to keep the current one.</param>
    /// <param name="forceRestart">Restart even if this clip is already playing.</param>
    public static void Play(Entity entity, int clipIndex,
                            bool? loop = null, bool forceRestart = false)
    {
        WriteCommand(entity, new AnimatedMeshCommand
        {
            Type = AnimatedMeshCommandType.ByIndex,
            ClipIndex = clipIndex,
            ForceRestart = forceRestart,
            OverrideLoop = loop.HasValue,
            Loop = loop ?? false,
        });
    }

    // ?? Play by clip name ?????????????????????????????????????????????????????

    /// <summary>Switch to the clip whose name matches <paramref name="clipName"/>.</summary>
    public static void Play(Entity entity, string clipName,
                            bool? loop = null, bool forceRestart = false)
    {
        WriteCommand(entity, new AnimatedMeshCommand
        {
            Type = AnimatedMeshCommandType.ByName,
            ClipNameHash = clipName.GetHashCode(),
            ForceRestart = forceRestart,
            OverrideLoop = loop.HasValue,
            Loop = loop ?? false,
        });
    }

    // ?? Playback control ??????????????????????????????????????????????????????

    public static void Pause(Entity entity) =>
        WriteCommand(entity, new AnimatedMeshCommand { Type = AnimatedMeshCommandType.Pause });

    public static void Resume(Entity entity) =>
        WriteCommand(entity, new AnimatedMeshCommand { Type = AnimatedMeshCommandType.Resume });

    /// <summary>Pause and rewind to frame 0.</summary>
    public static void Stop(Entity entity) =>
        WriteCommand(entity, new AnimatedMeshCommand { Type = AnimatedMeshCommandType.Stop });

    // ?? State queries (zero allocation) ??????????????????????????????????????

    public static bool GetState(Entity entity, out AnimatedMeshState state)
    {
        var em = World.DefaultGameObjectInjectionWorld?.EntityManager ?? default;
        if (em.Exists(entity) && em.HasComponent<AnimatedMeshState>(entity))
        {
            state = em.GetComponentData<AnimatedMeshState>(entity);
            return true;
        }
        state = default;
        return false;
    }

    /// <summary>Returns the name of the currently playing clip, or null on failure.</summary>
    public static bool GetClipName(Entity entity, out string name)
    {
        var em = World.DefaultGameObjectInjectionWorld?.EntityManager ?? default;
        if (em.Exists(entity)
            && em.HasComponent<AnimatedMeshState>(entity)
            && em.HasComponent<AnimatedMeshData>(entity))
        {
            var state = em.GetComponentData<AnimatedMeshState>(entity);
            var data = em.GetComponentObject<AnimatedMeshData>(entity);
            var clips = data.SO.Clips;
            if (state.ClipIndex >= 0 && state.ClipIndex < clips.Count)
            {
                name = clips[state.ClipIndex].Name;
                return true;
            }
        }
        name = null;
        return false;
    }

    /// <summary>Returns the number of clips available on this entity.</summary>
    public static bool GetClipCount(Entity entity, out int count)
    {
        var em = World.DefaultGameObjectInjectionWorld?.EntityManager ?? default;
        if (em.Exists(entity) && em.HasComponent<AnimatedMeshData>(entity))
        {
            var data = em.GetComponentObject<AnimatedMeshData>(entity);
            count = data.SO.Clips.Count;
            return true;
        }
        count = 0;
        return false;
    }

    // ?? Internal ??????????????????????????????????????????????????????????????

    private static void WriteCommand(Entity entity, AnimatedMeshCommand cmd)
    {
        var em = World.DefaultGameObjectInjectionWorld?.EntityManager ?? default;
        if (!em.Exists(entity) || !em.HasComponent<AnimatedMeshCommand>(entity))
        {
            Debug.LogWarning($"[AnimatedMeshController] Entity {entity} is invalid or missing AnimatedMeshCommand.");
            return;
        }
        em.SetComponentData(entity, cmd);
    }
}


// ???????????????????????????????????????????????????????????????????????????????
//  OPTIONAL MONOBEHAVIOUR WRAPPER
//  Attach to any GameObject in your scene to drive a specific entity
//  from the Inspector or UnityEvents without writing C# for simple cases.
// ???????????????????????????????????????????????????????????????????????????????

public class AnimatedMeshControllerMB : MonoBehaviour
{
    [Tooltip("Leave empty to control the entity baked from this very GameObject.")]
    [SerializeField] private GameObject Target;

    private Entity _entity;
    private bool _ready;

    private void Start()
    {
        var world = World.DefaultGameObjectInjectionWorld;
        if (world == null) return;

        var go = Target != null ? Target : gameObject;

        // The ConvertedEntityTable lets us map a GO to its baked Entity
        if (world.EntityManager.World.IsCreated)
        {
            // Works in Unity 6 with Entities package
            _entity = world.EntityManager
                           .CreateEntityQuery(typeof(AnimatedMeshTag))
                           .GetEntityQueryMask()
                           .Equals(default) ? Entity.Null : FindEntityForGameObject(go, world);
            _ready = _entity != Entity.Null;
        }
    }

    // Called e.g. from a UnityEvent button
    public void PlayByIndex(int index) => AnimatedMeshControllerECS.Play(_entity, index);
    public void PlayByName(string clipName) => AnimatedMeshControllerECS.Play(_entity, clipName);
    public void Pause() => AnimatedMeshControllerECS.Pause(_entity);
    public void Resume() => AnimatedMeshControllerECS.Resume(_entity);
    public void Stop() => AnimatedMeshControllerECS.Stop(_entity);

    // ?? Helpers ???????????????????????????????????????????????????????????????

    private static Entity FindEntityForGameObject(GameObject go, World world)
    {
        // Iterate entities with AnimatedMeshTag and match by Transform reference
        var em = world.EntityManager;
        var query = em.CreateEntityQuery(typeof(AnimatedMeshTag));
        var arr = query.ToEntityArray(Unity.Collections.Allocator.Temp);

        Entity result = Entity.Null;
        foreach (var e in arr)
        {
            if (!em.HasComponent<UnityEngine.Transform>(e)) continue;
            var t = em.GetComponentObject<Transform>(e);
            if (t != null && t.gameObject == go) { result = e; break; }
        }

        arr.Dispose();
        query.Dispose();
        return result;
    }
}